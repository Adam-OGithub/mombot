const makeCleanInput = theMsg => {
  const bannedChars = [
    `||`,
    `-`,
    `*`,
    `-`,
    `<>`,
    `<`,
    `>`,
    `,`,
    `=`,
    `<=`,
    `>=`,
    `~=`,
    `!=`,
    `^=`,
    `(`,
    `)`,
    `@`,
    `!`,
    `/`,
    `#`,
    `.`,
  ];

  const bannedArr = [
    'use',
    'show',
    'auth',
    'drop',
    'update',
    'delete',
    'get',
    'hello',
    'find',
    'from',
    'create',
    'rename',
    'db',
    'insert',
    'set',
    'stats',
  ];
  let cleanMsg = theMsg;
  bannedArr.forEach(entry => {
    const reg = new RegExp(`${entry}`, 'g');
    cleanMsg = cleanMsg.replace(reg, '');
  });
  const cleanRemove = cleanMsg
    .split('')
    .map(entry => {
      if (bannedChars.includes(entry)) {
        return '';
      } else {
        return entry;
      }
    })
    .join('');
  return cleanRemove;
};

exports.makeCleanInput = makeCleanInput;
