'use strict';

//Converts kelvin to fahrenheit
const convertKToF = K =>
  K !== undefined
    ? ((+K - 273.15) * 9) / 5 + 32
    : `Your guess is as good as mine.`;

//Converts meter to mile
const meterToMile = M =>
  M !== undefined
    ? `${(M * 2.236936).toFixed(2)}/mph`
    : `Your guess is as good as mine.`;

//Converts millimetters to inches
const milToIn = M => (M !== undefined ? (M * 0.03937).toFixed(2) : 0);

//Converts milliliters to oz
const millToOz = mil => (mil !== undefined ? (mil * 0.033814).toFixed(2) : mil);
//converts grams to oz
const gramToOz = gram =>
  gram !== undefined ? (gram * 0.03527396195).toFixed(2) : gram;
//converts kilos to lbs
const kiloToLb = kilo =>
  kilo !== undefined ? (kilo * 2.20462262185).toFixed(2) : kilo;

//converts degree to direction
const degToDir = deg => {
  let dir = ``;
  if (deg !== undefined) {
    if (deg >= 360 || (deg > 0 && deg <= 44)) {
      dir = `North`;
    } else if (deg >= 45 && deg <= 89) {
      dir = `North East`;
    } else if (deg >= 90 && deg <= 134) {
      dir = `East`;
    } else if (deg >= 135 && deg <= 179) {
      dir = `South East`;
    } else if (deg >= 180 && deg <= 224) {
      dir = `South`;
    } else if (deg >= 225 && deg <= 269) {
      dir = `South West`;
    } else if (deg >= 270 && deg <= 314) {
      dir = `West`;
    } else if (deg >= 315 && deg <= 359) {
      dir = `North West`;
    }
  } else {
    dir = deg;
  }
  return dir;
};

const timeToSeconds = (theType, theTime) => {
  let timeInSeconds = 0;
  switch (theType) {
    case 'minutes':
      timeInSeconds = theTime * 60;
      break;
    case 'hours':
      timeInSeconds = theTime * 60 * 60;
      break;
    case 'days':
      timeInSeconds = theTime * 24 * 60 * 60;
      break;
    case 'months':
      timeInSeconds = theTime * 30 * 24 * 60 * 60;
      break;
    default:
      break;
  }
  return timeInSeconds;
};

exports.convertKToF = convertKToF;
exports.meterToMile = meterToMile;
exports.milToIn = milToIn;
exports.millToOz = millToOz;
exports.gramToOz = gramToOz;
exports.kiloToLb = kiloToLb;
exports.degToDir = degToDir;
exports.timeInSeconds = timeToSeconds;
